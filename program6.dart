void main(){
  int x=4;
  switch(x){
    case 0:
      print("ZERO");
    case 1:
      print("ONE");
    case 2:
      print("TWO");
    case 3: 
      print("THREE"); 
    case 4:
      print("FOUR");
    case 5:
      print("FIVE");
    default:
      print("$x is greater then 5");
  }
}